//Operadores de igualdade/relacionais
	//"==" igualdade - Serve para testar se um valor é igual ao outro. Ex: 1 == 1 (o retorno desse comando será true, pois os valores são iguais).
	//"!=" - Com esse operador ele irá retornar se é verdadeiro que os valores são diferentes. Ex: 1 != 1 (O retorno desse comando será false, pois os valores não são diferentes).

		var x = 0

			x == 0 //(true)
			x != 0 //(false)

			"max" == "max" //(true)

		var nome = "max"

			nome == "carlos" //(false)

		var pessoa = "maria"

			nome == pessoa //(false)

		var umstring = "1"
		var umnumero = 1

			umstring == umnumero //(resultado será true, mesmo uma variavel sendo string e outra int, pois o JS é de tipagem fraca e faz a conversão automaticamente.)



