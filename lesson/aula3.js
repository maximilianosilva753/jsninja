//Mais operadores igualdade/relacional: Servem para testar também o TIPO;

	//'===' Igual a, e do mesmo tipo: 
	//'!==' Diferente, mas do mesmo tipo: 
		//Ex:
		 1 === '1'  //(retornará false, pois valida que são de valores iguais mas o tipo é diferente.)
		 1 !== '1'  //(retornará true, pois são de valores iguais mas o tipo é diferente.)
		 2 === 1    //(retornará false, pois são de tipos iguais mas os valores são diferentes.)
		"1" !== "1" //(retorna false pois são de tipos e valores iguais.) 


